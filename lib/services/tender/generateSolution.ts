import type { Phase, ProjectInput, Zone } from "@/lib/domain/tender";

export type GeneratedSolution = {
  summary: string;
  background: string;
  requirements: string[];
  objectives: string[];
  zoning: Zone[];
  implementationPlan: Phase[];
  operationsPlan: string[];
  riskControl: string[];
  acceptanceCriteria: string[];
};

function buildZones(input: ProjectInput): Zone[] {
  const zones: Zone[] = [
    {
      name: "有氧训练区",
      purpose: "提升心肺功能与日常训练覆盖率",
      areaRatio: 0.28,
      capacity: input.targetUsers
        ? Math.ceil(input.targetUsers * 0.35)
        : undefined,
    },
    {
      name: "力量训练区",
      purpose: "满足基础力量与综合训练需求",
      areaRatio: 0.3,
      capacity: input.targetUsers
        ? Math.ceil(input.targetUsers * 0.25)
        : undefined,
    },
    {
      name: "拉伸与恢复区",
      purpose: "用于训练前后热身、拉伸与康复辅助",
      areaRatio: 0.12,
    },
    {
      name: "储物与更衣区",
      purpose: "提供用户储物、更衣与基础配套",
      areaRatio: 0.12,
    },
    {
      name: "前台与管理区",
      purpose: "用于接待、门禁、登记和管理",
      areaRatio: 0.1,
    },
    {
      name: "辅材与设备缓冲区",
      purpose: "用于设备存放、维护及施工预留",
      areaRatio: 0.08,
    },
  ];

  if (input.siteType === "hospital") {
    zones.unshift({
      name: "低冲击康复区",
      purpose: "为医护与康复使用场景提供更温和的运动方案",
      areaRatio: 0.18,
      capacity: input.targetUsers
        ? Math.ceil(input.targetUsers * 0.2)
        : undefined,
    });
  }

  if (input.siteType === "factory") {
    zones.push({
      name: "班组快速训练区",
      purpose: "适合高频短时训练与轮换使用",
      areaRatio: 0.1,
    });
  }

  return zones;
}

