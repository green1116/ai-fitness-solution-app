export function generateSolution(input: ProjectInput): GeneratedSolution {
  const client = input.clientName?.trim() || "投标企业";
  const city = input.city?.trim() || "项目所在地（按招标文件）";
  const area = input.areaM2 != null ? `${input.areaM2} 平方米` : "面积以现场复核为准";
  const users =
    input.targetUsers != null
      ? `约 ${input.targetUsers} 人`
      : "服务人数以招标文件及现场复核为准";

  const requirements = [
    "严格对照招标文件技术条款与评分细则，形成可验证、可追溯的技术响应与证明材料索引。",
    "方案应具备投标可读性、交付完整性与预算可解释性：章节结构、术语口径、指标定义与附件引用保持一致。",
    "在缺少具体商品库的情况下，仍输出可执行的标准化建议配置，并明确品牌/型号替代原则与参数下限。",
    "统筹功能、成本、施工、运维与长期稳定性：对关键路径、资源投入与风险缓释措施给出可落地安排。",
    "对现场条件（配电、荷载、净高、排风、噪声、消防接口等）提出复核清单，避免实施阶段重大返工。",
    "对数据安全、隐私保护与设备使用合规性给出管理要求与责任边界（如涉及智能系统/联网设备）。",
    "对培训、验收、质保与售后服务给出可量化指标（响应时效、到场时限、备件策略等）。",
    "对变更管理、文档版本控制与会议纪要归档给出统一机制，确保投标承诺与合同履约一致。",
  ];

  const objectives = [
    "以“可评审、可交付、可运维”为目标，形成适用于企业投标场景的标准化方案文件体系（AI Fitness Solution）。",
    "在现有需求与约束条件下输出完整配置框架与实施路径，支撑技术标与商务标一致性表达。",
    "通过模块化与标准化设计，为后续商品系统接入预留可替换映射层，降低二次改造成本。",
    "以用户体验与安全管理为核心，形成动线合理、分区清晰、运维友好的空间与设备组合方案。",
    "以里程碑与质量控制点为抓手，确保关键节点可检查、可验收、可追责。",
    "以节能降耗与设备寿命为导向，提出运行策略与维保策略，提升全生命周期经济性。",
    "以评分项为导向组织证据链：技术参数、实施方案、服务承诺、企业能力材料相互印证。",
    "以风险前置为原则，对供货、施工、联调、移交等环节设置冗余与应急预案。",
  ];

  const summary = `${client}员工健身空间建设项目的投标技术方案，采用“标准化方案骨架 + 可替换配置明细”的编制方法：先确保章节齐全、指标清晰、响应可核验，再通过配置清单与实施计划形成闭环。方案覆盖空间规划、设备建议、实施组织、培训验收、运维移交与质保服务，品牌体系为 AI Fitness Solution，适用于正式投标评审与合同谈判阶段的技术澄清。`;

  const background = [
    `本项目面向企业级员工健身场景，场地类型为 ${input.siteType}，规划面积约 ${area}，服务规模 ${users}。`,
    `项目所在地：${city}；行业与使用特征将用于分区规划、设备组合与运维策略的适配。`,
    input.notes
      ? `招标补充与现场约束摘要：${input.notes}`
      : "现场约束与招标补充说明：以招标文件、答疑纪要及现场踏勘记录为准。",
    "建设目标：在满足安全与合规前提下，提升员工健康参与度与空间使用效率，并形成可持续运营的健身服务载体。",
    "编制原则：以招标文件为最高优先级，以可交付实施为底线，以评分项对齐为方法，以风险控制与责任边界清晰为约束。",
  ].join("");

  return {
    summary,
    background,
    requirements,
    objectives,
    zoning: buildZones(input),
    implementationPlan: buildImplementationPlan(input),
    operationsPlan: [
      "建立设备台账与资产编码体系，覆盖入库、安装、调拨、报废全生命周期。",
      "建立日常巡检、周检与月检机制，形成巡检路线、检查表与异常上报闭环。",
      "建立故障分级响应机制：一般问题远程指导，重大问题按 SLA 到场处置并留存记录。",
      "建立耗材与备件策略：关键备件清单、最低库存、供应商切换预案与到货周期跟踪。",
      "建立数据化运营看板（如适用）：使用率、能耗、故障率与课程满意度指标，支撑持续优化。",
      "建立安全管理制度：使用规范、应急演练、事故报告与责任追溯机制。",
      "建立供应商协同机制：安装、维保、升级与版本变更的统一接口人制度。",
      "建立年度复盘机制：对设备健康度、维保成本与用户体验进行复盘并形成改进计划。",
    ],
    riskControl: [
      "供货与交期风险：建立同等级备选品牌/型号清单，关键设备双供应商策略与到货里程碑跟踪。",
      "现场条件风险：对配电、荷载、净高、排风等进行前置复核，形成整改清单与责任分工。",
      "施工交叉与成品保护风险：明确施工窗口、工序衔接与保护措施，减少二次损伤与返工。",
      "联调与兼容性风险：制定联调测试用例与回退策略，确保系统稳定上线。",
      "验收口径风险：提前对齐验收标准、抽样方法与证据留存格式，避免争议。",
      "运维移交风险：培训覆盖管理员与关键用户，移交资料齐全并进行签字确认。",
      "预算与变更风险：对范围变更实行书面确认与影响评估，控制成本外溢。",
      "合规与安全风险：遵守消防、职业健康、数据安全等相关要求，形成检查清单。",
    ],
    acceptanceCriteria: [
      "技术响应完整性：对招标技术条款形成逐项响应矩阵，关键指标无遗漏、无矛盾。",
      "方案可实施性：空间、设备、施工、联调、培训、验收路径清晰且可执行。",
      "配置合理性：分区配置与人数/面积匹配，参数下限满足评审与使用安全要求。",
      "进度与里程碑：关键节点、工期假设、并行策略与缓冲设置合理。",
      "质量与过程控制：关键质量控制点、检查方法与记录模板齐全。",
      "安全与合规：涉及用电、结构荷载、消防接口、应急疏散等要求有明确响应。",
      "培训与移交：培训对象、频次、材料与考核方式明确；移交清单可核对。",
      "售后服务：质保范围、响应时效、到场时限、备件策略与升级机制可量化。",
      "商务一致性：技术方案与报价/服务条款口径一致，避免承诺冲突。",
      "资料与附件：附件索引清晰，版本号、日期与引用关系可追溯。",
    ],
  };
}
